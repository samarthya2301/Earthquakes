package com.samarthya.earthquakes;

public class Earthquake
{

	String mag, direction, place, date, time, url;

	Earthquake(String mag, String direction, String place, String date, String time, String url)
	{

		this.mag = mag;
		this.direction = direction;
		this.place = place;
		this.date = date;
		this.time = time;
		this.url = url;

	}

}
